import java.util.*;
public class NestesdIfElse{
    public static void main(String[] args) {
        int day=4;
        if(day==1){
            System.out.println("That day was Sunday");
        }
        else if(day==2){
            System.out.println("That day was Monday");
        }
        else if(day==3){
            System.out.println("That day was Tuesday");
        }
        else if(day==4){
            System.out.println("That day was Wednesday");
        }
        else if(day==5){
            System.out.println("That day was Thursday");
        }
        else if(day==6){
            System.out.println("That day was Friday");
        }
        else if(day==7){
            System.out.println("That day was Saturday");
        }
        else{
            System.out.println("Invalid day");
        }
    }

}
