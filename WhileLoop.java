import java.util.*;
public class WhileLoop{
    public static void main(String[] args) {
        int i=0;
        while(i<=48){
            System.out.println(i);
            i++; //increment operator (j++) = (j+=1)
        }
        System.out.println("Descending order of numbers from 48");
        int j=48;
        while(j>0){
            System.out.println(j);
            j--; //decrement operator (j--) = (j-=1)
        }
    }
}









